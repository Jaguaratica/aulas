// As funções resolvem esse problema ao encapsular um bloco de código que pode ser executado a qualquer momento, simplesmente "chamando" a função pelo seu nome. Os principais motivos para usar funções são:

// Reutilização de Código: Evita a repetição de lógica. Escreva uma vez, use quantas vezes precisar.

// Organização e Legibilidade: Divide um problema complexo em partes menores e mais gerenciáveis. Funções com nomes descritivos (como calcularAreaRetangulo()) tornam o código mais fácil de entender.

// Abstração: Oculta a complexidade de uma determinada tarefa. Você pode usar uma função sem precisar saber os detalhes de sua implementação.

// Manutenibilidade: Facilita a correção de bugs e a implementação de novas funcionalidades. Uma alteração na lógica precisa ser feita em um único lugar: dentro da função.

// Escopo: Cria um escopo local para as variáveis, evitando conflitos com outras partes do seu código.

// Quando Usar Funções?
